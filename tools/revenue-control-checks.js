function parseArgs(argv) {
  const args = {
    date: new Date().toISOString().slice(0, 10),
    limit: 10,
  };
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--date') {
      args.date = argv[++i];
    } else if (arg === '--limit') {
      args.limit = Number(argv[++i]);
    } else if (arg === '--help' || arg === '-h') {
      args.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return args;
}

function run(label, command, args) {
  const result = spawnSync(command, args, { encoding: 'utf8' });
  const ok = result.status === 0;
  console.log(`${ok ? '[OK]' : '[FAIL]'} ${label}`);
  if (!ok) {
    if (result.stdout) {
      process.stdout.write(result.stdout);
    }
    if (result.stderr) {
      process.stderr.write(result.stderr);
    }
  }
  return { label, ok, stdout: result.stdout || '', stderr: result.stderr || '' };
}

function runExpectFailure(label, command, args) {
  const result = spawnSync(command, args, { encoding: 'utf8' });
  const ok = result.status !== 0;
  console.log(`${ok ? '[OK]' : '[FAIL]'} ${label}`);
  if (!ok) {
    if (result.stdout) {
      process.stdout.write(result.stdout);
    }
    if (result.stderr) {
      process.stderr.write(result.stderr);
    }
  }
  return { label, ok, stdout: result.stdout || '', stderr: result.stderr || '' };
}

function discover(prefix, date) {
  return fs.readdirSync(process.cwd())
    .filter((name) => name.startsWith(prefix))
    .filter((name) => name.endsWith('.tsv'))
    .filter((name) => name.includes(date))
    .filter((name) => !name.includes('.example.'))
    .sort();
}

function nextDate(date) {
  const value = new Date(`${date}T00:00:00Z`);
  if (Number.isNaN(value.getTime())) {
    throw new Error(`Invalid date: ${date}`);
  }
  value.setUTCDate(value.getUTCDate() + 1);
  return value.toISOString().slice(0, 10);
}

function parseTsv(path) {
  const text = fs.readFileSync(path, 'utf8').trim();
  if (!text) {
    return [];
  }
  const lines = text.split(/\r?\n/);
  const headers = lines.shift().split('\t');
  return lines.map((line, index) => {
    const values = line.split('\t');
    if (values.length !== headers.length) {
      throw new Error(`${path} line ${index + 2}: expected ${headers.length} fields, got ${values.length}`);
    }
    const row = {};
    headers.forEach((header, columnIndex) => {
      row[header] = values[columnIndex];
    });
    return row;
  });
}

function offerName(route) {
  const match = String(route || '').match(/^(.+?)\s*\(/);
  return match ? match[1].trim() : String(route || '').trim();
}

function realPaymentLink(value) {
  return /^https:\/\/buy\.stripe\.com\/[A-Za-z0-9]/.test(String(value || ''));
}

function linkReadyOfferMap(path) {
  const byOffer = new Map();
  if (!fs.existsSync(path)) {
    return byOffer;
  }
  for (const row of parseTsv(path)) {
    byOffer.set(row.offer, row.status === 'ready' && realPaymentLink(row.payment_link_url));
  }
  return byOffer;
}

function requireText(result, pattern, label, failures) {
  const text = `${result.stdout}\n${result.stderr}`;
  if (pattern.test(text)) {
    console.log(`[OK] ${label}`);
  } else {
    console.log(`[FAIL] ${label}`);
    failures.push(label);
  }
}

function requireFileText(path, pattern, label, failures) {
  const text = fs.existsSync(path) ? fs.readFileSync(path, 'utf8') : '';
  if (pattern.test(text)) {
    console.log(`[OK] ${label}`);
  } else {
    console.log(`[FAIL] ${label}`);
    failures.push(label);
  }
}

function requireFileNotText(path, pattern, label, failures) {
  const text = fs.existsSync(path) ? fs.readFileSync(path, 'utf8') : '';
  if (!pattern.test(text)) {
    console.log(`[OK] ${label}`);
  } else {
    console.log(`[FAIL] ${label}`);
    failures.push(label);
  }
}

function extractFileCount(path, pattern) {
  if (!fs.existsSync(path)) {
    return null;
  }
  const match = fs.readFileSync(path, 'utf8').match(pattern);
  return match ? Number(match[1]) : null;
}

function requireMatchingCounts(left, right, label, failures) {
  if (Number.isFinite(left) && Number.isFinite(right) && left === right) {
    console.log(`[OK] ${label}`);
  } else {
    console.log(`[FAIL] ${label}`);
    failures.push(label);
  }
}

