#!/opt/homebrew/bin/node

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// Ensure Homebrew and common CLI paths are in PATH for launchd environment
process.env.PATH = '/opt/homebrew/bin:/usr/local/bin:/usr/bin:/bin:/usr/sbin:/sbin:' + (process.env.PATH || '');

const REPO_DIR = '/Users/igorganapolsky/workspace/git/igor/mac-yolo-safeguards';
const MOBILE_DIR = path.join(REPO_DIR, 'hermes-mobile');

function runCmd(cmd, cwd = REPO_DIR) {
  try {
    return execSync(cmd, { cwd, encoding: 'utf8' }).trim();
  } catch (error) {
    console.error(`Command failed: ${cmd}\nError:`, error.message);
    return null;
  }
}

async function handleDependabotAlerts() {
  console.log('Checking Dependabot alerts...');
  const alertsJson = runCmd('gh api repos/IgorGanapolsky/mac-yolo-safeguards/dependabot/alerts');
  if (!alertsJson) {
    console.log('No alerts found or failed to fetch alerts.');
    return;
  }

  let alerts = [];
  try {
    alerts = JSON.parse(alertsJson);
  } catch (e) {
    console.error('Failed to parse alerts JSON:', e.message);
    return;
  }

  const openAlerts = alerts.filter(a => a.state === 'open');
  if (openAlerts.length === 0) {
    console.log('No open Dependabot alerts.');
    return;
  }

  console.log(`Found ${openAlerts.length} open Dependabot alerts.`);
  let updatedAny = false;
  
  const packageJsonPath = path.join(MOBILE_DIR, 'package.json');
  if (!fs.existsSync(packageJsonPath)) {
    console.error('package.json not found in hermes-mobile.');
    return;
  }
  const pkg = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
  if (!pkg.overrides) {
    pkg.overrides = {};
  }

  for (const alert of openAlerts) {
    const pkgName = alert.security_vulnerability.package.name;
    const patchedVersion = alert.security_vulnerability.first_patched_version?.identifier;
    if (!patchedVersion) {
      console.log(`No patched version available for ${pkgName}. Skipping.`);
      continue;
    }

    if (pkg.overrides[pkgName] !== patchedVersion) {
      console.log(`Updating override: ${pkgName} -> ${patchedVersion}`);
      pkg.overrides[pkgName] = patchedVersion;
      updatedAny = true;
    }
  }

  if (!updatedAny) {
    console.log('All alerts already handled in overrides.');
    return;
  }

  fs.writeFileSync(packageJsonPath, JSON.stringify(pkg, null, 2) + '\n');
  console.log('Saved package.json with updated overrides.');

  console.log('Running npm install...');
  const installRes = runCmd('npm install', MOBILE_DIR);
  if (installRes === null) {
    console.error('npm install failed.');
    return;
  }

  console.log('Running tests...');
  const testRes = runCmd('npm test -- --runInBand', MOBILE_DIR);
  if (testRes === null) {
    console.error('Tests failed after updating overrides. Reverting package.json changes.');
    runCmd('git checkout package.json package-lock.json', MOBILE_DIR);
    return;
  }

  console.log('Tests passed! Committing changes...');
  const branchName = `security/dependabot-autofix-${Date.now()}`;
  runCmd(`git checkout -b ${branchName}`);
  runCmd('git add package.json package-lock.json', MOBILE_DIR);
  runCmd('git commit -m "security: override vulnerable packages to patch Dependabot alerts"');
  runCmd(`git push origin ${branchName}`);
  
  console.log('Creating PR...');
  runCmd(`gh pr create --head ${branchName} --title "security: override vulnerable packages to patch Dependabot alerts" --body "Autogenerated by Hermes Repo Watcher agent because of open Dependabot alerts."`);
  
  runCmd('git checkout main');
}

async function handlePullRequests() {
  console.log('Checking open pull requests...');
  const prsJson = runCmd('gh pr list --json number,title,isDraft,mergeable,statusCheckRollup,headRefName');
  if (!prsJson) {
    console.log('No PRs found or failed to fetch PRs.');
    return;
  }

  let prs = [];
  try {
    prs = JSON.parse(prsJson);
  } catch (e) {
    console.error('Failed to parse PRs JSON:', e.message);
    return;
  }

  for (const pr of prs) {
    console.log(`Analyzing PR #${pr.number}: "${pr.title}"`);
    if (pr.isDraft) {
      console.log(`PR #${pr.number} is a draft. Skipping.`);
      continue;
    }

    if (pr.mergeable !== 'MERGEABLE') {
      console.log(`PR #${pr.number} is not mergeable (state: ${pr.mergeable}). Skipping.`);
      continue;
    }

    const checks = pr.statusCheckRollup || [];
    const pendingChecks = checks.filter(c => c.status !== 'COMPLETED');
    if (pendingChecks.length > 0) {
      console.log(`PR #${pr.number} has pending checks. Skipping.`);
      continue;
    }

    const failedChecks = checks.filter(c => c.conclusion !== 'SUCCESS' && c.conclusion !== 'NEUTRAL' && c.conclusion !== 'SKIPPED');
    if (failedChecks.length > 0) {
      console.log(`PR #${pr.number} has failed checks: ${failedChecks.map(c => c.name).join(', ')}. Skipping.`);
      continue;
    }

    console.log(`PR #${pr.number} is green and mergeable! Merging autonomously...`);
    const mergeRes = runCmd(`gh pr merge ${pr.number} --squash --body "Merged autonomously by Hermes Repo Watcher agent because all CI checks passed successfully."`);
    if (mergeRes !== null) {
      console.log(`PR #${pr.number} successfully merged!`);
    }
  }
}

async function main() {
  try {
    console.log('Fetching origin main...');
    runCmd('git fetch origin main');

    await handleDependabotAlerts();
    await handlePullRequests();

    console.log('Watcher run completed successfully.');
  } catch (error) {
    console.error('Watcher run failed:', error);
  }
}

main();
